// Helper function to get content by section
export function getContentBySection(section: string): ContentItem[] {
  return defaultWebsiteContent.filter((item) => item.section === section);
}

// Helper function to get a specific content item
export function getContentValue(section: string, key: string): string {
  const item = defaultWebsiteContent.find((item) => item.section === section && item.key === key);
  return item?.value || "";
}
